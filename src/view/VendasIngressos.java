    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
        if(Integer.valueOf(this.jTextField5.getText()) > 0){                    
        
            atualizarContaAPagar();
            String[][] informacoesVendasEfetuadas = new String[1][10];

            String NomeComprador = "Não informado";
            NomeComprador = JOptionPane.showInputDialog(null, "Informar o nome do comprador?","Informar dados do comprador", JOptionPane.QUESTION_MESSAGE);
            if (NomeComprador == null){
                NomeComprador = "Não informado";                     
            }        
            Date dataAtual = new Date();                       
            informacoesVendasEfetuadas[0][0] = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 0).toString();
            informacoesVendasEfetuadas[0][1] = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 1).toString();
            informacoesVendasEfetuadas[0][2] = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 2).toString();                
            informacoesVendasEfetuadas[0][3] = this.jLabel3.getText();
            informacoesVendasEfetuadas[0][4] = this.jTextField5.getText();
            informacoesVendasEfetuadas[0][5] = this.jTextField7.getText();
            informacoesVendasEfetuadas[0][6] = this.jComboBox1.getSelectedItem().toString();
            informacoesVendasEfetuadas[0][7] = this.jComboBox3.getSelectedItem().toString();
            informacoesVendasEfetuadas[0][8] = NomeComprador;
            informacoesVendasEfetuadas[0][9] = dataAtual.toString();
            if (Vendas.adicionarNovaCompra(informacoesVendasEfetuadas)){
                JOptionPane.showMessageDialog(null, "A venda foi efetivada com sucesso!");                                        
                JFrame TicketImpresso = new Ticket();            
                Ticket.indiceVenda = control.Vendas.novaVendaIndice;                
                Ticket.reabrirVendas = true;            
                TicketImpresso.setVisible(true);                                    
                atualizaContador();
                preparaFormularioNovaVenda();                                
                this.dispose();
            }            
        }   else JOptionPane.showMessageDialog(null,"Você não especificou as cadeiras para a compra!");
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

