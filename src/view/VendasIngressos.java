    private void atualizarMostradorCadeiras(){
        
        int calculaNumeroTickets;
        String cadeirasCompradas;                
                   
        calculaNumeroTickets=0; 
        cadeirasCompradas = "";
        
//cadeirasOcupadas 
        //0 - Livre
        //1 - Ocupada
        //2 - Reservada                                      
        //Atualiza o marcador
        
        int SessaoAtiva = 0;
        SessaoAtiva = this.jTable1.getSelectedRow();
        
        for (int i = SessaoAtiva; i < SessaoAtiva+1; i++) {            
            for (int j= 0; j<50;j++){
                if (cadeirasOcupadas[SessaoAtiva][j] == 2){
                    calculaNumeroTickets = calculaNumeroTickets + 1; 
                    if (calculaNumeroTickets > 1){
                        cadeirasCompradas = cadeirasCompradas + ", " + retornaNumeroCadeira(j) ;
                    } else {
                        cadeirasCompradas =  retornaNumeroCadeira(j) ;
                    }
                    
                } else cadeirasOcupadas[SessaoAtiva][j] = 1;
            
                
            }            
        }              
        this.jLabel3.setText(cadeirasCompradas);                
        this.jTextField5.setText(String.valueOf(calculaNumeroTickets));                        
        atualizarContaAPagar();        
        if (calculaNumeroTickets >0){
            this.jButton1.setEnabled(true);
        }
        this.jLabel1.setText(String.valueOf(Cadeiras.QuantidadeAtualCadeirasLivres(SessaoAtiva)));
    }
    

